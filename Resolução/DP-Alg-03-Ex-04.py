x = int(input("Digite a quantidade de lados do polígono regular. (de 3 a 10) "))
if x == 3:
    print("O polígono é um triângulo. ")
elif x == 4:
    print("O polígono é um quadrado. ")
elif x == 5:
    print("O polígono é um pentágono. ")
elif x == 6:
    print("O polígono é um hexágono. ")
elif x == 7:
    print("O polígono é um heptágono. ")
elif x == 8:
    print("O polígono é um octógono. ")
elif x == 9:
    print("O polígono é um nonágono. ")
elif x == 10:
    print("O polígono é um decágono. ")
else:
    print("Erro. ")